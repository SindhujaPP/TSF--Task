datas=read.csv("LR Dataset.csv")
install.packages("ggplot2")
library(ggplot2)
head(datas)
plot(datas)
hist(datas$Scores)
lm=lm(Scores~Hours,data=datas)
summary(lm)
set.seed(100)
trainingrowindex=sample(1:nrow(datas),0.8*nrow(datas))
traingdata=datas[trainingrowindex,]
testdata=datas[-trainingrowindex,]
lmmodel=lm(Scores~Hours,data=traingdata)
distpred=predict(lmmodel,testdata)
summary(lmmodel)
actuals_preds=data.frame(cbind(actuals=testdata$Scores,predicteds=distpred))
correlation_accuracy=cor(actuals_preds)
head(actuals_preds)
predict(lm,data.frame(Hours=9.25))
